export const getLocalizedProvince = (name: string, lang: Language): string => {
  const upper = name?.trim().toUpperCase();
  if (!upper || !PROVINCE_MAP[upper]) return name;
  return PROVINCE_MAP[upper][lang] || name;
}

export const getLocalizedAdminName = (name: string, lang: Language): string => {
  if (!name) return name;
  let cleanName = name.trim().toUpperCase();
  let prefix = "";

  const prefixes: Record<string, Record<Language, string>> = {
    "KABUPATEN ": { id: "Kab. ", en: "Regency of ", zh: "县 ", ko: "군 ", ja: "郡 ", ar: "منطقة ", ms: "Daerah " },
    "KOTA ": { id: "Kota ", en: "City of ", zh: "市 ", ko: "시 ", ja: "市 ", ar: "مدينة ", ms: "Bandar " },
    "KECAMATAN ": { id: "Kec. ", en: "District ", zh: "区 ", ko: "구 ", ja: "区 ", ar: "ناحية ", ms: "Mukim " },
    "KELURAHAN ": { id: "Kel. ", en: "Village ", zh: "村 ", ko: "동 ", ja: "村 ", ar: "قرية ", ms: "Kampung " },
    "DESA ": { id: "Desa ", en: "Village ", zh: "村 ", ko: "리 ", ja: "村 ", ar: "قرية ", ms: "Kampung " }
  };

  for (const [p, trans] of Object.entries(prefixes)) {
    if (cleanName.startsWith(p)) {
      cleanName = cleanName.replace(p, "").trim();
      prefix = trans[lang] || p;
      break;
    }
  }

  const directions: Record<string, Record<Language, string>> = {
    " TIMUR": { id: " Timur", en: " East", zh: "东 ", ko: " 동", ja: "東 ", ar: " الشرقية", ms: " Timur" },
    " BARAT": { id: " Barat", en: " West", zh: "西 ", ko: " 서", ja: "西 ", ar: " الغربية", ms: " Barat" },
    " UTARA": { id: " Utara", en: " North", zh: "北 ", ko: " 북", ja: "北 ", ar: " الشمالية", ms: " Utara" },
    " SELATAN": { id: " Selatan", en: " South", zh: "南 ", ko: " 남", ja: "南 ", ar: " الجنوبية", ms: " Selatan" },
    " TENGAH": { id: " Tengah", en: " Central", zh: "中 ", ko: " 중", ja: "中 ", ar: " الوسطى", ms: " Tengah" },
    " TENGGARA": { id: " Tenggara", en: " Southeast", zh: "东南 ", ko: " 동남", ja: "東南 ", ar: " الجنوب الشرقي", ms: " Tenggara" },
  };

  for (const [d, trans] of Object.entries(directions)) {
    if (cleanName.endsWith(d)) {
      cleanName = cleanName.replace(d, "").trim();
      const suffix = trans[lang] || d;

      if (lang === "zh" || lang === "ja") {
        cleanName = suffix.trim() + cleanName;
      } else {
        cleanName = cleanName + suffix;
      }
      break;
    }
  }

  const formattedName = cleanName.split(" ").map(w => w.charAt(0) + w.slice(1).toLowerCase()).join(" ");

  if (lang === "en" || lang === "id" || lang === "ms") {
    return prefix + formattedName;
  } else if (lang === "ar") {
    return prefix + formattedName;
  } else {

    return formattedName + prefix.trim();
  }
}
